<div class="content">
	<h1>Vascserkész</h1>
		<h3>Mi a Vascserkész?</h3>	
		<p>
			Vascserkész egy váltó jellegű verseny, amelyben 6 tagú csoportok vesznek részt. 
			A csoportoknak 24 órájuk van arra, hogy végig menjenek az <b style="opacity:1; font-size: 17px;">50km</b>-es távon. <br>  
			Ezen a távon lesznek különböző versenyszámok, mint  <b style="opacity:1; font-size: 17px;"> 47 km </b> kerékpározás,  <b style="opacity:1; font-size: 17px;">2 km</b> futás, 
			<b style="opacity:1; font-size: 17px;">1 km</b> kajakozás,<br>  <b style="opacity:1; font-size: 17px;">800 m</b> úszás,  <b style="opacity:1; font-size: 17px;">20 m</b>-ről való céllövés </b>. 
			Ezeket mindig csak egy ember végzi a csoportból. 
			Miközben a csoport egyik tagja teljesíti a feladatát, a többiek is egy plusz feladatot teljesítenek. 
		</p>
		
		<h3>Feltételek</h3>
			<ol>
				<li>Az őrsök nevet választanak a versenyben.</li>
				<li>Csak  <b style="opacity:1; font-size: 17px;">avatott</b> cserkész indulhat.</li>
				<li>Egy őrs kialakítása 5 - 6 cserkészből áll. </li>
				<li>Az őrsök max. 3 embert pótólhatnak más csapatokból.</li>
				<li>Min. 4 cserkésznek az őrsből kell befejeznie a versenyt. Ha kevesebben lesznek, sérülés vagy más okokból az őrsöt kizárjuk a versenyből (Nevezési díj visszatérítés nélkül).</li>
				<li>A csapattagok kora min. 15 év. Korengedmény nincs!</li>
				<li>Minden 18 év alatti résztvevőnek kötelező magával hoznia a szülői beleegyezést és csapatparancsnoki ajánlást is.</li>
				<li>Egészségügyi dobozt minden őrs hozzon magával. Az EÜ Team ezt a felszerelést ajánja <a href="file/ládák.pdf" target="_blank">klik</a>.</li>
				<li>A hátizsák súlya az egész verseny alatt min. 12kg. Ebben a súlyban nincs belszámítva a víz és az élelmiszer sem. A hátizsák előírt felszerelését megtaláljátok a versenyzámokban.</li>
				<li>A hátizsákot a verseny ideje alatt mindenkinek viselnie kell. Kivétel: kajakozás, úszás, futás és kerékpározás.</li>
				<li>Aki nem tartja be a szabályokat, azt kizárjuk a versenyből.</li>
			</ol>
