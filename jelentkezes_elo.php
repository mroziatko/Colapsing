<h1>Vascserkész jelentkezés</h1>
<p style="margin:0% 7% 0% 0%;">A Vascserkész egy hattusa verseny, amit a versenycsapatoknak 24 óra alatt kell teljesíteniük. A verseny 100 km hosszú, amiből 50 km gyaloglás – ezt minden versenyzőnek teljesítenie kell. 
Az egyéni versenyszámok: 46 km kerékpározás; 1 km kajakozás; 2 km futás; 800 m úszás és célba lövés 20 méterről.

<br>Egyéb tudnivalók:
<br><br>
• A jelentkezés 2 körből fog állni. Az első körben a cserkészcsapatok bejelentik a versenycsapataikat és befizetik a nevezési díj első felét, ami 10€/fő. A jelentkezés második része a résztvevők személyének pontosítását jelenti majd és a nevezési díj másik felének befizetését, ami várhatólag 5-7€ lesz fejenként.
<br><br>
• A versenyen 5-6 fős versenycsapatok indulhatnak.
<br><br>
• A versenyt a versenycsapat legalább 4 tagjának teljesítenie kell. 
<br><br>
• Ha egy cserkészcsapat nem tud teljes versenycsapatot indítani, feltölthetik ezt legfeljebb 3 másik cserkészcsapatból. 
<br><br>
• A jelentkezés a részvételi díj első felének befizetésével lesz érvényes, ami 10€/fő. Ezt legkésőbb 2014. május 30-ig kell megtenni (23:59.59-ig)
</p>


